function openSlideMenu(){
    document.getElementById('side-menu').style.width = '200px';
    document.getElementById('about').style.marginLeft = '200px';
    document.getElementById('services').style.marginLeft = '200px';
    document.getElementById('portfolio').style.marginLeft = '200px';
    document.getElementById('myCarousel').style.marginLeft = '200px';
    document.getElementById('pricing').style.marginLeft = '200px';
    document.getElementById('contact').style.marginLeft = '200px';
    document.getElementById('footer').style.marginLeft = '200px';
}

function closeSlideMenu(){
    document.getElementById('side-menu').style.width = '0';
    document.getElementById('about').style.marginLeft = '0';
    document.getElementById('services').style.marginLeft = '0';
    document.getElementById('portfolio').style.marginLeft = '0';
    document.getElementById('myCarousel').style.marginLeft = '0';
    document.getElementById('pricing').style.marginLeft = '0';
    document.getElementById('contact').style.marginLeft = '0';
    document.getElementById('footer').style.marginLeft = '0';
}